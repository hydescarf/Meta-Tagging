function UpdateTags( _data ){
	let regex = /[\.,;。、\n；\s]*[\.,;。、\n；][\.,;。、\n；\s]*/g
	let data = _data.replace(regex, ';'); // replace commas/semicolon to semicolon, also trim whitespaces around them.
	
	if( data[data.length-1] != ";" ){
		data += ";"; //auto add a semicolon at the end if there's none
	}
	if( data[0] == "," || data[0] == " " ){
		data = data.slice(1); // remove first letter of ^ if there's any
	}
	
	data = RemoveRepeatedTags( data );
	data = data.replace(/\s\s+/g, ' '); // replace multiple spaces into one
	//data = data.replace(regex, ';'); // repeat
	
	return data;
}
function GetTagsCount( _data ){
	return _data.split(";").length-1;
}
function RemoveRepeatedTags( _data ){
	let old_data = _data.split(";");
	let new_data = Array.from(new Set(old_data));
	return new_data.join(";");
}

function SplitTags( _data, ){
	let tag_array = _data.split(";"); //turn string into array
	if( tag_array[tag_array.length-1] == "" ) tag_array.splice(tag_array.length-1,1); //remove the last keyword that is empty because of ; at the end
	
	return tag_array;
}

function CompareTags( _data, count_number, keep_dupe=false ){ //Keep unique, remove duplicated ;; return STRING
	let tag_array = _data.split(";"); //turn string into array.
	
	let count = {};
	let result;
	
	if( keep_dupe ){
		result = tag_array.filter((item) => { // count and filter in item that has more than count_number
			count[item] = (count[item] || 0) + 1;
			return count[item] >= count_number;
		});
	}
	else{
		result = tag_array.filter((item) => { // count first as it's not possible to filter in one go
			count[item] = (count[item] || 0) + 1;
			return count[item] < count_number;
		});
		result = result.filter((item)=>{ // now we filter in with a further filtered result
			return count[item] < count_number;
		});
	}
	
	let new_data = Array.from(new Set(result));
	return new_data.join(";");
}

function RemoveDupeTags( _data ){
	let tag_array = _data.split(";"); //turn string into array.
	
	let result = tag_array.filter((item, index, arr) => {
		return arr.indexOf(item) === arr.lastIndexOf(item);
	});
	
	let new_data = Array.from(new Set(result));
	return new_data.join(";");
}













