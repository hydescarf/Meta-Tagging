var Tooltip = new class{
	current_text = "";
	active = false;
	parent_info;
	tooltip_info;
	dialogue_tail = "bottom";
	
	call( dom, text ){
		if( !text ) return;
		this.current_text = text;
		this.active = true;
		this.parent_info = dom.getBoundingClientRect();
	}
	
	close(){
		this.active = false;
	}
	
	get_visibility(){
		return (this.active ? "" : "_hidden") + " tooltip_" + this.dialogue_tail;
	}
	
	onupdate(vnode){
		if( !this.active ) return;
		
		this.tooltip_info = vnode.dom.getBoundingClientRect();		
		
		let top = this.parent_info.y-this.parent_info.height-13;
		this.dialogue_tail = "bottom";
		vnode.dom.classList.add("tooltip_bottom");
		vnode.dom.classList.remove("tooltip_top");
		
		if( top <= 0 ){
			top =  this.parent_info.y+this.parent_info.height+13;
			this.dialogue_tail = "top";
			vnode.dom.classList.add("tooltip_top");
			vnode.dom.classList.remove("tooltip_bottom");
		}
		
		vnode.dom.style = `top:${top}px; left:${this.parent_info.x+(this.parent_info.width/2)-(this.tooltip_info.width/2)}px;`;
	}
	
	view(vnode){
		return [
			m("div", {class:"tooltip "+this.get_visibility()}, this.current_text)
		]
	}
}


